// SaveBeat : ""
func (s *Service) SaveBeat(ctx *models.Context, beat *models.Beat) error {
	log.Println("transaction start")
	// Start Transaction
	if err := ctx.Session.StartTransaction(); err != nil {
		return err
	}
	defer ctx.Session.EndSession(ctx.CTX)
	beat.UniqueID = s.Daos.GetUniqueID(ctx, constants.COLLECTIONBEAT)
	beat.Status = constants.BEATSTATUSSTART
	t := time.Now()
	created := models.Created{}
	created.On = &t
	created.By = constants.SYSTEM
	log.Println("b4 Beat.created")
	beat.Created = &created
	beat.StartTime = &t
	beat.Date = &t
	log.Println("b4 Beat.created")
	if err := mongo.WithSession(ctx.CTX, ctx.Session, func(sc mongo.SessionContext) error {
		beatmaster, err := s.Daos.GetSingleBeatMaster(ctx, beat.BeatMasterID)
		if err != nil {
			return errors.New("error in getting the Vechilelog- " + err.Error())
		}
		beat.AssignBeatdetails = beatmaster.BeatMaster
		dberr := s.Daos.SaveBeat(ctx, beat)
		if dberr != nil {
			return dberr
		}

		if err := ctx.Session.CommitTransaction(sc); err != nil {
			return errors.New("Not able to commit - " + err.Error())
		}
		return nil
	}); err != nil {
		log.Println("Transaction start aborting")
		if abortError := ctx.Session.AbortTransaction(ctx.CTX); abortError != nil {
			return errors.New("Error while aborting transaction" + abortError.Error())
		}
		log.Println("Transaction aborting completed successfully")
		return err
	}
	return nil
}

